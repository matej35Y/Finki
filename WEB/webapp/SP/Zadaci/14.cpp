using namespace std;

int main()
{
    int n,m;
    cin>>n>>m;
    double nova[n][m];
    double matrix[n][m];
    
    for (int i=0;i<n;i++){
        for (int j=0;j<m;j++){
            cin>>matrix[i][j];
        }
    }
    for(int j=0;j<m;j++){
        int min=100;
        int max=0;
        for (int i=0;i<n;i++){
            if(matrix[i][j]<min){
                min=matrix[i][j];
            }
            if (matrix[i][j]>max) {
                max=matrix[i][j];
            }
        }
        for(int i=0;i<n;i++){
               nova[i][j]=double(matrix[i][j]-min)/(max-min);
                
        }
    }
    
    for (int i=0;i<n;i++){
        for (int j=0;j<m;j++){
            printf("%.2f ", nova[i][j]);
        }
        cout<<endl;
    }
}
